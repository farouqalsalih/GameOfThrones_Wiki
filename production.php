                The series began development in January 2007.[5] HBO acquired the television rights to the novels, with Benioff and Weiss as the series' executive producers and Martin as a co-executive producer. The intention was for each novel to yield a season's worth of episodes.[5] Initially, Martin would write one episode per season while Benioff and Weiss would write the rest.[5] Jane Espenson and Bryan Cogman were added later to write one episode each for the first season.[52] The first and second drafts of the pilot script by Benioff and Weiss were submitted in August 2007[53] and June 2008,[54] respectively. Although HBO liked both drafts,[55] a pilot was not ordered until November 2008.[56] The pilot episode, "Winter Is Coming", was shot in 2009; after its poor reception following a private viewing, HBO demanded an extensive re-shoot (about 90 percent of the episode, with cast and directorial changes).[49][57] The pilot reportedly cost HBO $5–10 million to produce,[58] while the first season's budget was estimated at $50–60 million.[59] For the second season, the series received a 15-percent budget increase for the climactic battle in "Blackwater" (which had an $8 million budget).[60][61] Between 2012 and 2015, the average budget per episode increased from $6 million[62] to "at least" $8 million.[63] The sixth-season budget was over $10 million per episode, for a season total of over $100 million, a record for a series's production cost.[64] By the final season, the production budget per episode was estimated to be $15 million.[65]
